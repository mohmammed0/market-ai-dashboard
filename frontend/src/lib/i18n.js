function translatePattern(value) {
  if (/^(\d+)\s+recent quote snapshots$/i.test(value)) {
    return value.replace(/^(\d+)\s+recent quote snapshots$/i, "$1 لقطة سعر حديثة");
  }

  if (/^(\d+)\s+scheduler jobs configured$/i.test(value)) {
    return value.replace(/^(\d+)\s+scheduler jobs configured$/i, "$1 مهمة مجدولة");
  }

  if (/^(\d+)\s+advancing vs\s+(\d+)\s+declining$/i.test(value)) {
    return value.replace(/^(\d+)\s+advancing vs\s+(\d+)\s+declining$/i, "$1 صاعد مقابل $2 هابط");
  }

  if (/^ML\s+(on|off)$/i.test(value)) {
    return value.replace(/^ML\s+on$/i, "ML يعمل").replace(/^ML\s+off$/i, "ML متوقف");
  }

  if (/^DL\s+(on|off)$/i.test(value)) {
    return value.replace(/^DL\s+on$/i, "DL يعمل").replace(/^DL\s+off$/i, "DL متوقف");
  }

  if (/^(.+)\s+last$/i.test(value)) {
    return value.replace(/^(.+)\s+last$/i, "$1 آخر سعر");
  }

  if (/^\d+\s+rows$/i.test(value)) {
    return value.replace(/^(\d+)\s+rows$/i, "$1 صف");
  }

  if (/^\d+\s+Symbols$/i.test(value)) {
    return value.replace(/^(\d+)\s+Symbols$/i, "$1 رمز");
  }

  if (/^\d+\s+indices$/i.test(value)) {
    return value.replace(/^(\d+)\s+indices$/i, "$1 مؤشر");
  }

  if (/^\d+\s+ticks$/i.test(value)) {
    return value.replace(/^(\d+)\s+ticks$/i, "$1 تحديث");
  }

  if (/^Rank #/i.test(value)) {
    return value.replace(/^Rank #/i, "الترتيب #");
  }

  if (/^Conf /i.test(value)) {
    return value.replace(/^Conf /i, "الثقة ");
  }

  if (/^Prev /i.test(value)) {
    return value.replace(/^Prev /i, "الإغلاق السابق ");
  }

  if (/^Source /i.test(value)) {
    return value.replace(/^Source /i, "المصدر ");
  }

  if (/^Round lot /i.test(value)) {
    return value.replace(/^Round lot /i, "حجم اللوت ");
  }

  if (/^Vol /i.test(value)) {
    return value.replace(/^Vol /i, "الحجم ");
  }

  if (/^(.+)\s+Daily Price$/i.test(value)) {
    return value.replace(/^(.+)\s+Daily Price$/i, "$1 السعر اليومي");
  }

  return value;
}

function translateComposite(value) {
  if (value.includes(" · ")) {
    return value
      .split(" · ")
      .map((part) => t(part))
      .join(" · ");
  }

  if (value.includes(" | ")) {
    return value
      .split(" | ")
      .map((part) => t(part))
      .join(" | ");
  }

  return value;
}

